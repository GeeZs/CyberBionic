package JavaEssential.HomeWork_002_ClassesAndObjects.App002;

class Car {
    private int year;
    private String color;

    Car() {
    }

    Car(int year) {
        this.year = year;
    }

    Car(String color) {
        this.color = color;
    }

    Car(int year, String color) {
        this.year = year;
        this.color = color;
    }

}
